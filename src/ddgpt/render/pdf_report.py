# Cover Page

def build_cover_page(
    story,
    styles,
    risk_score,
    facts_df=None,
    data_quality=None,
    recommendation=None
):

    story.append(
        Spacer(1, 1.6 * inch)
    )

    story.append(
        Paragraph(
            "DDGPT",
            styles["CoverTitle"]
        )
    )

    story.append(
        Paragraph(
            "Investment Committee Memorandum",
            styles["SectionTitle"]
        )
    )

    story.append(
        Spacer(1, 0.4 * inch)
    )

    story.append(
        Paragraph(
            f"""
            <b>Generated:</b>
            {datetime.now().strftime("%B %d, %Y")}
            """,
            styles["Body"]
        )
    )

    story.append(
        Paragraph(
            f"""
            <b>Risk Score:</b>
            {risk_score:.2f}
            """,
            styles["Body"]
        )
    )

    if recommendation:
        story.append(
            Paragraph(
                f"""
                <b>Recommendation:</b>
                {recommendation.get("decision", "N/A")}
                (confidence {recommendation.get("confidence", 0.0):.0%})
                """,
                styles["Body"]
            )
        )

    if facts_df is not None and len(facts_df):
        doc_names = ", ".join(str(n) for n in facts_df["doc_name"].tolist())
        story.append(
            Paragraph(
                f"""
                <b>Documents analyzed ({len(facts_df)}):</b>
                {doc_names}
                """,
                styles["Body"]
            )
        )

    if data_quality:
        found = sum(r["fields_found"] for r in data_quality)
        total = sum(r["fields_total"] for r in data_quality)
        confidences = [r["avg_confidence"] for r in data_quality if r["avg_confidence"] is not None]
        avg_conf = sum(confidences) / len(confidences) if confidences else None
        conf_text = f"{avg_conf:.0%}" if avg_conf is not None else "N/A"

        story.append(
            Paragraph(
                f"""
                <b>Data completeness:</b>
                {found}/{total} core fields extracted, average confidence {conf_text}
                """,
                styles["Body"]
            )
        )

    story.append(
        Spacer(1, 0.4 * inch)
    )

    story.append(
        Paragraph(
            """
            Confidential institutional diligence report generated
            using DDGPT autonomous diligence workflows.
            """,
            styles["Small"]
        )
    )

    story.append(PageBreak())


