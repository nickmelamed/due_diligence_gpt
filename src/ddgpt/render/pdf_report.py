# Evidence Table

def build_evidence_table(
    story,
    styles,
    facts_df
):

    story.append(
        Paragraph(
            "Extracted Metrics",
            styles["SectionTitle"]
        )
    )

    table_data = [[
        "Document",
        "Net IRR",
        "Target IRR",
        "TVPI",
        "Mgmt Fee",
        "Carry",
        "Confidence"
    ]]

    conf_cols = [c for c in ("aum_conf", "net_irr_conf", "mgmt_fee_conf", "carry_conf") if c in facts_df.columns]
    tier_by_row = []

    for _, row in facts_df.iterrows():

        confs = [row[c] for c in conf_cols if pd.notna(row[c])]
        avg_conf = (sum(confs) / len(confs)) if confs else None
        tier = _confidence_tier(avg_conf)
        tier_by_row.append(tier)

        table_data.append([
            Paragraph(html.escape(str(row["doc_name"]), quote=False), styles["TableCell"]),
            str(row["net_irr_pct"]),
            str(row["target_irr_pct"]),
            str(row["tvpi"]),
            str(row["mgmt_fee_pct"]),
            str(row["carry_pct"]),
            tier if avg_conf is None else f"{tier} ({avg_conf:.0%})"
        ])

    table = Table(
        table_data,
        colWidths=[
            2.1 * inch,
            0.8 * inch,
            0.9 * inch,
            0.7 * inch,
            0.8 * inch,
            0.7 * inch,
            1.1 * inch
        ]
    )

    style_commands = [

        (
            "BACKGROUND",
            (0, 0),
            (-1, 0),
            colors.HexColor("#111827")
        ),

        (
            "TEXTCOLOR",
            (0, 0),
            (-1, 0),
            colors.white
        ),

        (
            "FONTNAME",
            (0, 0),
            (-1, 0),
            "Helvetica-Bold"
        ),

        (
            "GRID",
            (0, 0),
            (-1, -1),
            0.5,
            colors.HexColor("#d1d5db")
        ),

        (
            "ROWBACKGROUNDS",
            (0, 1),
            (-1, -1),
            [
                colors.white,
                colors.HexColor("#f3f4f6")
            ]
        ),

        (
            "BOTTOMPADDING",
            (0, 0),
            (-1, 0),
            10
        ),

        (
            "TOPPADDING",
            (0, 0),
            (-1, -1),
            8
        ),

        (
            "BOTTOMPADDING",
            (0, 1),
            (-1, -1),
            8
        ),

        (
            "VALIGN",
            (0, 0),
            (-1, -1),
            "TOP"
        )

    ]

    # Confidence column (last column) gets its tier color as text, so a
    # reviewer can see data quality at a glance without opening the
    # Streamlit "Under the Hood" audit view.
    for row_idx, tier in enumerate(tier_by_row, start=1):
        style_commands.append(
            ("TEXTCOLOR", (-1, row_idx), (-1, row_idx), CONFIDENCE_COLORS[tier])
        )
        style_commands.append(
            ("FONTNAME", (-1, row_idx), (-1, row_idx), "Helvetica-Bold")
        )

    table.setStyle(TableStyle(style_commands))

    story.append(table)

    story.append(
        Spacer(1, 24)
    )


# Data Quality Section

def build_data_quality_section(
    story,
    styles,
    data_quality
):
    if not data_quality:
        return

    story.append(
        Paragraph(
            "Data Quality & Evidence Verification",
            styles["SectionTitle"]
        )
    )

    story.append(
        Paragraph(
            "How each metric's citation was checked against the source document: "
            "a verbatim match, a fuzzy match (OCR noise/hyphenation), or no match found.",
            styles["Small"]
        )
    )

    story.append(Spacer(1, 8))

    table_data = [[
        "Document",
        "Fields Found",
        "Avg Confidence",
        "Fuzzy Matches",
        "Not Found"
    ]]

    for row in data_quality:
        avg_conf = row["avg_confidence"]
        table_data.append([
            Paragraph(html.escape(str(row["doc_name"]), quote=False), styles["TableCell"]),
            f'{row["fields_found"]}/{row["fields_total"]}',
            "N/A" if avg_conf is None else f"{avg_conf:.0%}",
            str(row["fuzzy_matches"]),
            str(row["not_found"]),
        ])

    table = Table(
        table_data,
        colWidths=[2.6 * inch, 1.1 * inch, 1.2 * inch, 1.1 * inch, 1.0 * inch]
    )

    table.setStyle(
        TableStyle([
            ("BACKGROUND", (0, 0), (-1, 0), colors.HexColor("#111827")),
            ("TEXTCOLOR", (0, 0), (-1, 0), colors.white),
            ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
            ("GRID", (0, 0), (-1, -1), 0.5, colors.HexColor("#d1d5db")),
            ("ROWBACKGROUNDS", (0, 1), (-1, -1), [colors.white, colors.HexColor("#f3f4f6")]),
            ("TOPPADDING", (0, 0), (-1, -1), 8),
            ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
            ("VALIGN", (0, 0), (-1, -1), "TOP"),
        ])
    )

    story.append(table)
    story.append(Spacer(1, 24))


# Memo Body

HEADING_RE = re.compile(r"^(#{1,6})\s+(.*)$")
BULLET_RE = re.compile(r"^[-*]\s+(.*)$")
NUMBERED_RE = re.compile(r"^\d+\.\s+(.*)$")
HR_RE = re.compile(r"^-{3,}$")


def build_memo_body(
    story,
    styles,
    memo
):

    story.append(
        Paragraph(
            "Full IC Memorandum",
            styles["SectionTitle"]
        )
    )

    memo = strip_code_fences(memo)
    lines = memo.split("\n")

    for line in lines:

        line = line.strip()

        if not line:
            story.append(Spacer(1, 8))
            continue

        if HR_RE.match(line):
            story.append(Spacer(1, 6))
            continue

        heading_match = HEADING_RE.match(line)
        if heading_match:
            level = len(heading_match.group(1))
            text = format_inline(heading_match.group(2).strip())
            style = styles["SectionTitle"] if level <= 2 else styles["SubHeading"]
            story.append(Paragraph(text, style))
            continue

        bullet_match = BULLET_RE.match(line)
        if bullet_match:
            story.append(
                Paragraph(f"• {format_inline(bullet_match.group(1))}", styles["CustomBullet"])
            )
            continue

        numbered_match = NUMBERED_RE.match(line)
        if numbered_match:
            story.append(
                Paragraph(format_inline(line), styles["CustomBullet"])
            )
            continue

        story.append(
            Paragraph(format_inline(line), styles["Body"])
        )


# Main Render

def render_ic_pdf(
    output_path: str,
    memo: str,
    flags,
    facts_df,
    risk_score,
    extracted=None,
    recommendation=None
):
    data_quality = compute_data_quality(extracted) if extracted else None

    styles = build_styles()

    doc = SimpleDocTemplate(
        output_path,
        pagesize=letter,
        rightMargin=48,
        leftMargin=48,
        topMargin=48,
        bottomMargin=48
    )

    story = []

    build_cover_page(
        story,
        styles,
        risk_score,
        facts_df=facts_df,
        data_quality=data_quality,
        recommendation=recommendation
    )

    build_flags_section(
        story,
        styles,
        flags
    )

    build_evidence_table(
        story,
        styles,
        facts_df
    )

    build_data_quality_section(
        story,
        styles,
        data_quality
    )

    build_memo_body(
        story,
        styles,
        memo
    )

    doc.build(
        story,
        onFirstPage=add_watermark,
        onLaterPages=add_watermark
    )